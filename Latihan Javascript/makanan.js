var item = prompt("Masukkan makanan atau minuman : \n (Contoh : daging, nasi, susu, teh");

switch (item) {
  case "nasi":
    alert("Ini adalah makanan");
    break;

  case "daging":
    alert("Ini adalah makanan");
    break;

  case "teh":
    alert("Ini adalah minuman");
    break;

  case "susu":
    alert("Ini adalah minuman");
    break;

  default:
    alert("Anda salah memasukkan");
    break;
}
